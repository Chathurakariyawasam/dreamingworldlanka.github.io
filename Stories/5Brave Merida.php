<h1>Merida</h1>
<p>
King Fergus and Queen Elinor ruled the kingdom of DunBroch in Scotland.
 They had triplet sons and one daughter, the beautiful Princess Merida.
 All four children were mischievous!
 <br> <br>
 Queen Elinor tried to teach Merida how to behave like a perfect princess.
 But Merida liked to slip away from the palace to practice with her bow and arrow.
 She was a skilled archer.
 <br> <br>
 One night, the queen announced that three clans were coming to visit. 
 Each would present a suitor to compete for Merida's hand in marriage.
 Merida was horrified.
 <br> <br>
  "I won't go through with it!" she shouted.
 To Merida's dismay, the suitors arrived as planned.
 Merida sat waiting on her throne for the archery contest to begin.
 <br> <br>
 She knew she could handle a bow and arrow far better than the suitors could!
 Once the suitors had taken their shots, Merida marched onto the field.
 The queen yelled for her to stop, but Merida didn't listen.
 <br> <br>
 She fired her arrows and hit each target dead center. 
 She had won! Now she wouldn't have to marry anyone!
 Queen Elinor was furious at Merida for ruining the contest.
 She warned that things needed to be set right.
 <br> <br>
 "Marriage is what YOU want!" Merida shouted. 
 Then she slashed the tapestry that depicted her family and ran away.
 Merida fled deep into the woods where mysterious blue will o' the wisps led her to a cottage.
 Inside, she found a witch. Merida begged her to change her mother. The Witch gave Merida a spell cake.
 <br> <br>
 Back at the castle, Merida presented the cake to her mother. She watched closely as Elinor took a bite.
 At first, nothing happened. But then, Elinor began to feel sick.
 Merida helped Elinor into bed. The next thing Merida knew, a huge, furry shape was rising from the sheets!
 The Witch's cake had turned Elinor into a bear!
 <br> <br>
 Worried her mother was in danger, Merida sneaked her out of the castle.
 Merida and Elinor-Bear first went to the Witch's cottage and only found a message:
 "Fate be changed, look inside, mend the bond torn by pride."
 <br> <br>
Merida would need time to figure out its meaning, but for now, she needed to teach her mother to fish!
Merida realized that to mend the bond and break the spell, she must repair the tapestry she had torn.
After sneaking into the castle, Merida and her mother were discovered by King Fergus.
The king drew his sword. "It's not what you think!" Merida cried.
<br> <br>
The king wouldn't listen. He slashed at the bear, who struck back, knocking him to the ground.
The noise drew the visiting lords and their sons. Terrified, Elinor-Bear raced from the castle.
Merida grabbed a needle and thread and the tapestry then jumped on her horse.
She took her little brothers, too. They had eaten the spell cake and turned into bear cubs!
 Merida sewed as they all raced to save their mother.
 <br> <br>
 When Merida arrived, she wrapped the mended tapestry over her mother.
 "I want you back, Mum," Merida cried. "I love you."
 King Fergus was shocked to realize the bear was his beloved wife.
 <br> <br>
 Merida felt a hand brush her hair. The spell had been broken! Elinor was human once again.
 The triplets were no longer bears.
 The queen smiled at Merida. From then on, Merida could be a princess AND be herself.
 

