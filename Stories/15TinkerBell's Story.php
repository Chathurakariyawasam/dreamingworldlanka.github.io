                "You fixed it!" Silvermist cried. They were all amazed at her tinkering talent. Tinker Bell enjoyed tinkering. But she still wanted to go to the mainland. At her last hope, Tink went to Vidia for help. But, Vidia was still angry with her. Vidia got an evil idea. She said Tink should capture the Sprinting Thistles to prove she was a garden fairy. It was a dangerous job. 
