                The bird made a nosedive for Tink. "Hawk!" yelled the fairies as they ran for cover. Tink jumped into a hole to hide. The hole was Vidia's hiding spot. Now, the hawk was after Vidia, too.
                The other fairies attacked the hawk with berries. Vidia was safe but she was angry. Tink tried to help her clean up but she didn't want Tink's help. 
