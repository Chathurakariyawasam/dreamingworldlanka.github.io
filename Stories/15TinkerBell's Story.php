                "Being a tinker stinks," said Tinker Bell back at Tinker's Nook. Fairy Mary told her that she should be proud of who she was. But Tinker Bell did not want to be a tinker. She wanted to be a nature fairy. She asked her friends for help. At first, Silvermist tried to teach Tink how to be a water fairy. But Tink was not good with water. 
