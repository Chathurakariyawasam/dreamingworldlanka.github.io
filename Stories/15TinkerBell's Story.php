                Then, Iridessa tried to teach Tink how to be a light fairy, but Tink was not good with light. Fawn tried to show Tink how to be an animal fairy, but Tink was not good with animals, either. Tink saw a big bird flying in the sky. "Maybe that guy can help!" she said. 
