<h1>Peter Pan</h1>
            <p> 
                  Once upon a time, there were three children Wendy, John and Michael Darling. They liked bedtime because every time in the nursery Wendy told stories about Peter Pan.
                  <br> <br>
                   Peter is a little boy who decided never to grow up. He lives in a far away Never Land, full of adventures and fun. The children loved to hear stories about him and Peter himself would come flying down and sit on the nursery window sill to hear the stories. One night Peter asked the children to come with him to the Never Land.
                   <br> <br>
                 Wendy was delighted on hearing this. Peter taught them how to fly. All it took was a wish and a pinch of pixie dust and a little practice too. Then out of the nursery they flew away to the Never Land. The Never Land was a wonderful place. There were fairies living in treetops.
                 <br> <br>
                There were mermaids swimming in a lagoon. There were real red Indians in a village on a cliff. There were woods full of wild animals. Best of all, there were ships full of pirates with a specially wicked leader Captain Hook. Wendy, John and Michael knew at first sight that they would love the Never Land.
                <br> <br>
                 They liked Peter's underground house, with lots of hidden doorways in a big hollow tree. There they met the Lost Boys who shared Peter's home. All the boys were delighted that Wendy had come to tell bedtimes stories to them but they didn't spend much time in that underground house. 
                 <br> <br>
                 There were too many exciting things to do. Sometimes they played at war with the red Indians, who were their good friends. Sometimes they had trouble with the wicked pirates, who were their enemies.
                  One day the pirates stole away Princess Tiger Lily of the Indian tribe.
                <br> <br>
                The Indian Chief, her father, all were upset but Peter Pan rescued Tiger Lily and brought her safely home again. This made Captain Hook, the leader of the pirates mad at Peter, more than ever before. He kidnapped Wendy, John, Michael and all the lost boys while Peter was away.
                <br> <br>
                 He took them to his pirate ship. Hook decided to throw them off from the ship. Wendy was sure that Peter would rescue them. At the last minute, Peter appeared. He beat Captain Hook and freed every one of his friends. They scared those bad pirates into jumping overboard and rowing away in their boat. Now, they got the pirate ship. 
                 <br> <br>
                 Wendy decided to go back home. With a wish and a pinch of pixie dust, they made the pirate ship fly! They all sailed on that ship through the sky to the nursery window again. The children's parents could hardly believe that their children had been to Never Land. Wendy, John and Michael never forgot Peter Pan even after they grew up.
                





